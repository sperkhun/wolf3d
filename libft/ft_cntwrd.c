int		ft_cntwrd(const char *s, char c)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	if (*s != c)
		i++;
	while (*s)
	{
		if (*s == c && *(s + 1) != c && *(s + 1) != '\0')
			i++;
		s++;
	}
	return (i);
}
